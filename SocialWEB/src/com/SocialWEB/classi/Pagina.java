package com.SocialWEB.classi;

import java.util.Date;

public class Pagina 
{
	
	String documentoHTML="";
	String utente="";
	Date dataGenerazione;
	
	public String getDocumentoHTML() {
		return documentoHTML;
	}
	public void setDocumentoHTML(String documentoHTML) {
		this.documentoHTML = documentoHTML;
	}
	public String getUtente() {
		return utente;
	}
	public void setUtente(String utente) {
		this.utente = utente;
	}
	public Date getDataGenerazione() {
		return dataGenerazione;
	}
	public void setDataGenerazione(Date date) {
		this.dataGenerazione = date;
	}
	
	
}
